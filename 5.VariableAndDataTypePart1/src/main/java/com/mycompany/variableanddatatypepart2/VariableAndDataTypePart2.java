package com.mycompany.variableanddatatypepart2;

public class VariableAndDataTypePart2 {

    public static void main(String[] args) {
        
        boolean x = true; //Dynamic Initialization//
        char s = 'y';
        short p = 1001;
        int z = 10;
        float r = 1.2f;
        double t = 3.1416;
        
        
        System.out.println("x = "+x);
        System.out.println("s = "+s);
        System.out.println("p = "+p);
        System.out.println("z = "+z);
        System.out.println("r = "+r);
        System.out.println("t = "+t);
    }
}
